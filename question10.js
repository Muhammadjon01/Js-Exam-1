function monthAndSeason(n){
    switch(n){
        case 1:
            console.log("Jan, Winter")
        break;
        case 2:
            console.log("Feb, Winter")
        break;
        case 3:
            console.log("Mar, Winter")
        break;
        case 4:
            console.log("Apr, Spring")
        break;
        case 5:
            console.log("May, Spring")
        break;
        case 6:
            console.log("June, Sprring")
        break;
        case 7:
            console.log("July, Summer")
        break;
        case 8:
            console.log("Aug, Summer")
        break;
        case 9:
            console.log("Sep, Summer")
        break;
        case 10:
            console.log("Oct, Autumn")
        break;
        case 11:
            console.log("Nov, Autumn")
        break;
        case 12:
            console.log("Dec, Autumn")
        break;
    }
}

monthAndSeason(5)